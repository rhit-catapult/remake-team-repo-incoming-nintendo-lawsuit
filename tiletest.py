def rendermap():
    map_display.fill((146, 244, 255))
    y = 0
    tile_rects = []
    for row in game_map:
        x = 0
        for tile in row:
            if tile != 0:
                tile_rects.append(pygame.Rect(x*tilesize, y*tilesize, tilesize, tilesize))
            if tile == 1:
                map_display.blit(grass_image, (x*tilesize, y*tilesize))
            if tile == 2:
                map_display.blit(dirt_image, (x*tilesize, y*tilesize))
            x += 1
        y += 1